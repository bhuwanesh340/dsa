d1 = {"a":1, "b":2, "c":3}

d2 = {"b":2, "c":3, "a":1}

print(d1 == d2)
# dictionary equality (==) checks both keys and their corresponding values, regardless of the order of insertion.

# In Python, dictionaries are inherently unordered collections, so the order of keys does not affect equality.
# Therefore, d1 and d2 are considered equal because they contain the same key-value pairs





